# main.py

import tkinter as tk

# 1. Создаем главное окно приложения
root = tk.Tk()

# 2. Устанавливаем заголовок окна
root.title("Привет, мир!")

# 3. Устанавливаем начальный размер окна (ширина x высота)
root.geometry("400x200")

# 4. Создаем текстовую метку с нашей фразой
#   - root: где разместить метку (в главном окне)
#   - text: какой текст показать
#   - font: какой шрифт использовать
label = tk.Label(root, text="Привет, мир!", font=("Arial", 24))

# 5. Размещаем метку в окне, чтобы она была видна
#    (pack() - это самый простой способ, он просто добавляет элемент в окно)
label.pack(expand=True)

# 6. Запускаем главный цикл приложения.
#    Окно будет отображаться и ждать действий пользователя, пока его не закроют.
root.mainloop()
